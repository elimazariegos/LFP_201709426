    public void actualizar_img() {

        if (noticia.getImagen()[0] != null) {
            ImageIcon img = new ImageIcon(getClass().getResource(noticia.getImagen()[0]));
            Icon fd = new ImageIcon(img.getImage().getScaledInstance(lbl_img_uno.getWidth(),
                    lbl_img_uno.getHeight(), Image.SCALE_DEFAULT));
            lbl_img_uno.setIcon(fd);
            lbl_img_uno.setText(null);
        }
        if (noticia.getImagen()[1] != null) {
            ImageIcon img = new ImageIcon(getClass().getResource(noticia.getImagen()[1]));
            Icon fd = new ImageIcon(img.getImage().getScaledInstance(lbl_img_dos.getWidth(),
                    lbl_img_dos.getHeight(), Image.SCALE_DEFAULT));
            lbl_img_dos.setIcon(fd);
            lbl_img_dos.setText(null);

        }
        if (noticia.getImagen()[2] != null) {
            ImageIcon img = new ImageIcon(getClass().getResource(noticia.getImagen()[2]));
            Icon fd = new ImageIcon(img.getImage().getScaledInstance(lbl_img_tres.getWidth(),
                    lbl_img_tres.getHeight(), Image.SCALE_DEFAULT));
            lbl_img_tres.setIcon(fd);
            lbl_img_tres.setText(null);
        }

    }


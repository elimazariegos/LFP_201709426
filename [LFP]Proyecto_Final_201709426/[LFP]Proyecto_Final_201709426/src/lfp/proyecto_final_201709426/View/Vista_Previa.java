    public void guardarComo() throws IOException {
        generar_archivo();
        JFileChooser guardar = new JFileChooser();

        guardar.showSaveDialog(null);
        FileFilter filtro = new FileNameExtensionFilter("Archivos lop (.lop)", "lop");

        guardar.setFileFilter(filtro);
        guardar.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);

        File archivo = guardar.getSelectedFile();
        FileWriter escribir;
        try {

            escribir = new FileWriter(archivo, true);
            escribir.write(texto);
            escribir.close();
            JOptionPane.showMessageDialog(null, "Se exporto correctamente el archivo");

        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Error al guardar, ponga nombre al archivo");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error al guardar, en la salida");
        }

    }

    public void generar_archivo() {

        texto = texto + "<" + noticia.getTipo() + ">\n";
        texto = texto + "<Titulo>" + noticia.getTitulo() + "</Titulo>\n";
        texto = texto + "<Autor>" + noticia.getAutor() + "</Autor>\n";
        texto = texto + "<Contenido>" + noticia.getContenido() + "\n";

        if (noticia.getImagen()[0] != null) {
            texto = texto + "<ImgUno>\"" + noticia.getImagen()[0] + "\"</ImgUno>\n";
        }

        if (noticia.getImagen()[1] != null) {
            texto = texto + "<ImgDos>\"" + noticia.getImagen()[1] + "\"</ImgDos>\n";
        }

        if (noticia.getImagen()[2] != null) {
            texto = texto + "<ImgTres>\"" + noticia.getImagen()[2] + "\"</ImgTres>\n";
        }
        texto = texto + "</Contenido>\n</" + noticia.getTipo() + ">";

    }


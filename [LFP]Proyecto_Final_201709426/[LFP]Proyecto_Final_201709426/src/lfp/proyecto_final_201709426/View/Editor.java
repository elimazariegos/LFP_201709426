    public void analizar() {
        if (!txt_editor.getText().isEmpty()) {
            new Analisis_Lexico(lista_token, errores_lexicos)
                    .analizar(txt_editor.getText());
            new Analisis_Sintactico(lista_token, errores_sintaciticos, noticia).etiquetas_principales();
            txt_consola.setText(txt_consola.getText() + "\n\nSe encontraror errores, es imposible visualizar o publicar la noticia\n\n");
            if (errores_lexicos.isEmpty() && errores_sintaciticos.isEmpty()) {
                btn_visualizar.setEnabled(true);
                btn_publicar.setEnabled(true);
                jmi_publicar.setEnabled(true);
                jmi_visualizar.setEnabled(true);
                txt_consola.setText("El analisis fue realizado con exito, no ese encontro  ningun error");
            }
            JOptionPane.showMessageDialog(null, "se abrira automaticamente el archivo donde se encuentran los resultados del analisis");
            new Reports(lista_token, errores_lexicos, errores_sintaciticos).crear_reporte();
        } else {
            JOptionPane.showMessageDialog(null, "El cuadro de texto se encuetra vacio");
        }

    }

